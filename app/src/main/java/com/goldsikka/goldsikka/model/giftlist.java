package com.goldsikka.goldsikka.model;

public class giftlist {

    private String payment_slipno;
    private String gold;
    private String amount;
    private String total_amount;
    private String reference;
    private String date;

    public String getPayment_slipno() {
        return payment_slipno;
    }

    public void setPayment_slipno(String payment_slipno) {
        this.payment_slipno = payment_slipno;
    }

    public String getGold() {
        return gold;
    }

    public void setGold(String gold) {
        this.gold = gold;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getTotal_amount() {
        return total_amount;
    }

    public void setTotal_amount(String total_amount) {
        this.total_amount = total_amount;
    }

    public String getReference() {
        return reference;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
